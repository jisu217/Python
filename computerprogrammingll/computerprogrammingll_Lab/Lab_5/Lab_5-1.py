cafeMenu =  "아메리카노", "카페라떼", "바닐라라뗴", "코코넛스무디"

print("[[메뉴]]")
num = 1
for m in cafeMenu:
    print(f"{num}. {m}")
    num = num + 1

# cafeMenu = [ "아메리카노", "카페라떼", "바닐라라떼", "코코넛스무디" ]
# 여기서는 cafeMenu가 리스트로 정의됩니다. 리스트는 대괄호 [ ]로 감싸며,
# 변경 가능한 자료형입니다. 즉, 리스트에 요소를 추가하거나 제거할 수 있습니다.

# cafeMenu = "아메리카노", "카페라떼", "바닐라라떼", "코코넛스무디"
# 여기서는 cafeMenu가 튜플로 정의됩니다. 튜플은 괄호 ( ) 없이도 생성할 수 있으며,
# 리스트와 달리 변경이 불가능한 자료형입니다. 즉, 한 번 정의된 튜플은 요소를 추가하거나 제거할 수 없습니다.

# 리스트 ([ ]): 변경 가능. 요소를 추가, 수정, 삭제 가능.
# 튜플 ( ( ) 없이도 정의 가능): 변경 불가능. 요소를 추가, 수정, 삭제 불가능.
# 하지만 두 경우 모두 for 루프를 사용하여 값을 출력하는 방식에서는 동일하게 작동합니다.